def _validate_ip(ip: str) -> bool:
    """Validate an IP address to prevent command injection."""
    try:
        ipaddress.ip_address(ip)
        return True
    except ValueError:
        return False


def _validate_ip_or_network(ip: str) -> bool:
    """Validate IP address or CIDR network."""
    try:
        ipaddress.ip_address(ip)
        return True
    except ValueError:
        pass
    try:
        ipaddress.ip_network(ip, strict=False)
        return True
    except ValueError:
        return False


# Track applied rules for rollback
_applied_rules: list[dict] = []
_applied_rules_lock = threading.Lock()


class ExecuteRequest(BaseModel):
    job_id: str = Field(..., description="Job ID with generated rules")
    rule_type: str = Field("iptables", description="iptables or nftables only (snort/suricata are file-based)")
    dry_run: bool = Field(True, description="If True, validate without applying")


class ExecuteResponse(BaseModel):
    executed: int
    failed: int
    dry_run: bool
    results: list[dict]
    rollback_id: str


def _execute_iptables_rule(rule_text: str, dry_run: bool = True) -> dict:
    """Execute a single iptables rule via subprocess."""
    # Only allow iptables -A INPUT rules generated by our system
    if not rule_text.startswith("iptables -A INPUT"):
        return {"rule": rule_text, "status": "skipped", "reason": "Only INPUT chain rules are allowed"}

    # Extract and validate the source IP
    ip_match = re.search(r'-s\s+(\S+)', rule_text)
    if not ip_match or not _validate_ip_or_network(ip_match.group(1)):
        return {"rule": rule_text, "status": "skipped", "reason": "Invalid or missing source IP"}

    if dry_run:
        return {"rule": rule_text, "status": "validated", "reason": "Dry run — rule validated but not applied"}

    try:
        result = subprocess.run(
            rule_text.split(),
            capture_output=True, text=True, timeout=10,
        )
        if result.returncode == 0:
            logger.info("Applied firewall rule: %s", rule_text)
            return {"rule": rule_text, "status": "applied", "reason": "Success"}
        else:
            logger.warning("Failed to apply rule: %s — %s", rule_text, result.stderr.strip())
            return {"rule": rule_text, "status": "failed", "reason": result.stderr.strip()}
    except subprocess.TimeoutExpired:
        return {"rule": rule_text, "status": "failed", "reason": "Timeout"}
    except PermissionError:
        return {"rule": rule_text, "status": "failed", "reason": "Permission denied — container needs NET_ADMIN capability"}
    except FileNotFoundError:
        return {"rule": rule_text, "status": "failed", "reason": "iptables binary not found"}


def _execute_nftables_rule(rule_text: str, dry_run: bool = True) -> dict:
    """Execute a single nftables rule via subprocess."""
    if not rule_text.startswith("nft add rule"):
        return {"rule": rule_text, "status": "skipped", "reason": "Only 'nft add rule' commands are allowed"}

    ip_match = re.search(r'ip saddr\s+(\S+)', rule_text)
    if not ip_match or not _validate_ip_or_network(ip_match.group(1)):
        return {"rule": rule_text, "status": "skipped", "reason": "Invalid or missing source IP"}

    if dry_run:
        return {"rule": rule_text, "status": "validated", "reason": "Dry run — rule validated but not applied"}

    try:
        result = subprocess.run(
            ["nft"] + rule_text[4:].split(),  # strip "nft " prefix
            capture_output=True, text=True, timeout=10,
        )
        if result.returncode == 0:
            logger.info("Applied nftables rule: %s", rule_text)
            return {"rule": rule_text, "status": "applied", "reason": "Success"}
        else:
            return {"rule": rule_text, "status": "failed", "reason": result.stderr.strip()}
    except (subprocess.TimeoutExpired, PermissionError, FileNotFoundError) as e:
        return {"rule": rule_text, "status": "failed", "reason": str(e)}


@router.post("/firewall/execute", response_model=ExecuteResponse)
def execute_firewall_rules(
    body: ExecuteRequest,
    user=Depends(require_role("admin")),
    db: Session = Depends(get_db),
):
    """
    Execute previously generated firewall rules (admin only).

    By default runs in dry_run=True mode (validates without applying).
    Set dry_run=False to actually apply rules to the host.
    Requires NET_ADMIN capability in the container.
    """
    from database import FirewallRule
    rules = (
        db.query(FirewallRule)
        .filter(FirewallRule.job_id == body.job_id)
        .all()
    )
    if not rules:
        raise HTTPException(404, f"No generated rules found for job {body.job_id}")

    if body.rule_type not in ("iptables", "nftables"):
        raise HTTPException(400, "Only iptables and nftables can be executed directly. Snort/Suricata rules are file-based.")

    executor = _execute_iptables_rule if body.rule_type == "iptables" else _execute_nftables_rule
    results = []
    executed = 0
    failed = 0
    rollback_id = secrets.token_hex(6)

    for rule in rules:
        if rule.rule_type != body.rule_type:
            continue
        res = executor(rule.rule_text, dry_run=body.dry_run)
        results.append(res)
        if res["status"] == "applied":
            executed += 1
            with _applied_rules_lock:
                _applied_rules.append({
                    "rollback_id": rollback_id,
                    "rule_text": rule.rule_text,
                    "rule_type": body.rule_type,
                    "source_ip": rule.source_ip,
                    "applied_at": datetime.datetime.utcnow().isoformat(),
                    "applied_by": user.email,
                })
        elif res["status"] == "failed":
            failed += 1

    # Audit log
    _log_audit(db, user, "FIREWALL_EXECUTE",
               f"job={body.job_id} type={body.rule_type} dry_run={body.dry_run} "
               f"executed={executed} failed={failed}")

    logger.info(
        "Firewall execute: job=%s type=%s dry_run=%s executed=%d failed=%d user=%s",
        body.job_id, body.rule_type, body.dry_run, executed, failed, user.email,
    )

    return ExecuteResponse(
        executed=executed, failed=failed, dry_run=body.dry_run,
        results=results, rollback_id=rollback_id,
    )


@router.post("/firewall/rollback/{rollback_id}")
def rollback_firewall_rules(
    rollback_id: str,
    user=Depends(require_role("admin")),
    db: Session = Depends(get_db),
):
    """Rollback previously applied firewall rules by deleting them."""
    with _applied_rules_lock:
        to_rollback = [r for r in _applied_rules if r["rollback_id"] == rollback_id]

    if not to_rollback:
        raise HTTPException(404, f"No rules found for rollback ID {rollback_id}")

    results = []
    for entry in to_rollback:
        # Convert -A (append) to -D (delete) for rollback
        rule_text = entry["rule_text"]
        if entry["rule_type"] == "iptables":
            delete_cmd = rule_text.replace("iptables -A ", "iptables -D ", 1)
            try:
                result = subprocess.run(delete_cmd.split(), capture_output=True, text=True, timeout=10)
                status = "rolled_back" if result.returncode == 0 else "rollback_failed"
                results.append({"rule": rule_text, "status": status, "detail": result.stderr.strip()})
            except Exception as e:
                results.append({"rule": rule_text, "status": "rollback_failed", "detail": str(e)})

    with _applied_rules_lock:
        _applied_rules[:] = [r for r in _applied_rules if r["rollback_id"] != rollback_id]

    _log_audit(db, user, "FIREWALL_ROLLBACK", f"rollback_id={rollback_id} count={len(to_rollback)}")

    return {"rollback_id": rollback_id, "rolled_back": len(results), "results": results}


