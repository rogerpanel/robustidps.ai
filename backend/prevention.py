# Step executors — real actions mapped to playbook step types
def _step_rate_limit(src_ip: str, **kwargs) -> dict:
    """Apply rate limiting via iptables hashlimit."""
    if not _validate_ip(src_ip):
        return {"status": "skipped", "reason": "Invalid IP"}
    rule = (
        f"iptables -A INPUT -s {src_ip} -m hashlimit --hashlimit-above 50/sec "
        f'--hashlimit-name ridps_ratelimit -j DROP -m comment --comment "RobustIDPS-RateLimit"'
    )
    try:
        result = subprocess.run(rule.split(), capture_output=True, text=True, timeout=10)
        return {"status": "applied" if result.returncode == 0 else "failed", "detail": result.stderr.strip()}
    except Exception as e:
        return {"status": "failed", "detail": str(e)}


def _step_firewall(src_ip: str, action: str = "DROP", **kwargs) -> dict:
    """Apply iptables DROP/REJECT rule."""
    if not _validate_ip(src_ip):
        return {"status": "skipped", "reason": "Invalid IP"}
    rule = f'iptables -A INPUT -s {src_ip} -j {action} -m comment --comment "RobustIDPS-Enforce"'
    try:
        result = subprocess.run(rule.split(), capture_output=True, text=True, timeout=10)
        return {"status": "applied" if result.returncode == 0 else "failed", "detail": result.stderr.strip()}
    except Exception as e:
        return {"status": "failed", "detail": str(e)}


def _step_block_temp(src_ip: str, duration_min: int = 30, **kwargs) -> dict:
    """Temporary IP block with at-scheduled removal."""
    result = _step_firewall(src_ip, "DROP")
    if result["status"] == "applied":
        # Schedule removal (best-effort)
        try:
            remove_cmd = f'iptables -D INPUT -s {src_ip} -j DROP -m comment --comment "RobustIDPS-Enforce"'
            at_cmd = f'echo "{remove_cmd}" | at now + {duration_min} minutes'
            subprocess.run(["bash", "-c", at_cmd], capture_output=True, timeout=5)
            result["auto_expire_min"] = duration_min
        except Exception:
            result["auto_expire_min"] = None
            result["note"] = "Rule applied but auto-expire failed; manual removal needed"
    return result


def _step_alert_siem(src_ip: str, label: str, confidence: float, **kwargs) -> dict:
    """Forward alert to configured SIEM connectors."""
    try:
        from siem_connectors import emit_event
        event = {
            "threat_label": label,
            "severity": "critical",
            "src_ip": src_ip,
            "confidence": confidence,
            "model_used": "enforcement",
            "action_taken": "enforce",
        }
        emit_event(event)
        return {"status": "sent", "detail": "Alert forwarded to SIEM"}
    except Exception as e:
        return {"status": "failed", "detail": str(e)}


def _step_log(**kwargs) -> dict:
    """Log step — always succeeds."""
    return {"status": "logged"}


# Map step action names to executors
_STEP_EXECUTORS = {
    "rate_limit": _step_rate_limit,
    "firewall": _step_firewall,
    "block_temp": _step_block_temp,
    "block_perm": lambda src_ip, **kw: _step_firewall(src_ip, "DROP"),
    "block_c2": lambda src_ip, **kw: _step_firewall(src_ip, "DROP"),
    "alert": _step_alert_siem,
    "log": _step_log,
    "detect": lambda **kw: {"status": "detected"},
    "verify": lambda **kw: {"status": "verified"},
}


@router.post("/threat-response/enforce")
def enforce_playbook(
    body: EnforceRequest,
    user=Depends(require_role("admin")),
    db: Session = Depends(get_db),
):
    """
    Execute a threat response playbook with REAL enforcement actions (admin only).

    Set confirm=True to actually execute. Without confirm=True, returns
    a preview of what would be executed.
    """
    from threat_response import PLAYBOOKS, _get_all_playbooks

    all_pb = _get_all_playbooks(db)
    if body.playbook_id not in all_pb:
        raise HTTPException(404, f"Playbook not found: {body.playbook_id}")

    pb = all_pb[body.playbook_id]

    if not _validate_ip(body.source_ip):
        raise HTTPException(400, "Invalid source IP address")

    response_chain = pb.get("response_chain", [])
    threat_label = body.threat_label or (pb.get("trigger_classes", ["Unknown"])[0])

    if not body.confirm:
        # Preview mode
        preview_steps = []
        for step in response_chain:
            action = step.get("action", "")
            has_executor = action in _STEP_EXECUTORS
            preview_steps.append({
                **step,
                "can_enforce": has_executor,
                "enforcement_type": "real" if has_executor and action not in ("detect", "verify", "log") else "informational",
            })
        return {
            "mode": "preview",
            "playbook": pb["name"],
            "source_ip": body.source_ip,
            "steps": preview_steps,
            "warning": "Set confirm=True to execute these actions for real",
        }

    # Enforce mode — actually execute steps
    incident_id = f"ENF-{secrets.token_hex(4).upper()}"
    steps_executed = []

    for step in response_chain:
        action = step.get("action", "")
        executor = _STEP_EXECUTORS.get(action)

        if executor:
            result = executor(
                src_ip=body.source_ip,
                label=threat_label,
                confidence=body.confidence,
                action="DROP",
            )
            steps_executed.append({
                **step,
                "mode": "enforced",
                "result": result,
                "timestamp": datetime.datetime.utcnow().isoformat(),
            })
        else:
            steps_executed.append({
                **step,
                "mode": "skipped",
                "result": {"status": "no_executor", "detail": f"No real executor for action '{action}'"},
                "timestamp": datetime.datetime.utcnow().isoformat(),
            })

    # Persist incident
    inc = Incident(
        incident_id=incident_id,
        playbook_id=body.playbook_id,
        playbook_name=pb["name"],
        severity=pb.get("severity", "high"),
        source_ip=body.source_ip,
        target_ip=body.target_ip,
        threat_label=threat_label,
        confidence=body.confidence,
        mode="enforced",
        steps=steps_executed,
        total_simulated_ms=0,
        actual_execution_ms=0,
        effectiveness_score=pb.get("effectiveness_score", 0),
        false_positive_rate=pb.get("false_positive_rate", 0),
        triggered_by=user.email,
    )
    db.add(inc)
    db.commit()

    _log_audit(db, user, "THREAT_RESPONSE_ENFORCE",
               f"playbook={body.playbook_id} src_ip={body.source_ip} incident={incident_id}")

    return {
        "incident_id": incident_id,
        "mode": "enforced",
        "playbook": pb["name"],
        "source_ip": body.source_ip,
        "steps": steps_executed,
    }


