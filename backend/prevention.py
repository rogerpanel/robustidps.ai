_quarantine: dict[str, dict] = {}  # ip → {reason, quarantined_at, quarantined_by, ttl_min, expires_at}
_quarantine_lock = threading.Lock()


class QuarantineRequest(BaseModel):
    reason: str = Field("Threat detected", description="Reason for quarantine")
    ttl_minutes: int = Field(60, ge=1, le=1440, description="Auto-expire after N minutes (max 24h)")
    bidirectional: bool = Field(True, description="Block both inbound and outbound")


@router.post("/quarantine/{ip}")
def quarantine_ip(
    ip: str,
    body: QuarantineRequest,
    user=Depends(require_role("admin")),
    db: Session = Depends(get_db),
):
    """Quarantine an IP address — applies bidirectional firewall DROP rules (admin only)."""
    if not _validate_ip(ip):
        raise HTTPException(400, f"Invalid IP address: {ip}")

    with _quarantine_lock:
        if ip in _quarantine:
            raise HTTPException(409, f"IP {ip} is already quarantined")

    # Apply rules
    rules_applied = []
    inbound = f'iptables -A INPUT -s {ip} -j DROP -m comment --comment "RobustIDPS-Quarantine"'
    res_in = _execute_iptables_rule(inbound, dry_run=False)
    rules_applied.append({"direction": "inbound", **res_in})

    if body.bidirectional:
        outbound = f'iptables -A OUTPUT -d {ip} -j DROP -m comment --comment "RobustIDPS-Quarantine"'
        try:
            result = subprocess.run(outbound.split(), capture_output=True, text=True, timeout=10)
            rules_applied.append({
                "direction": "outbound",
                "rule": outbound,
                "status": "applied" if result.returncode == 0 else "failed",
                "reason": result.stderr.strip(),
            })
        except Exception as e:
            rules_applied.append({"direction": "outbound", "rule": outbound, "status": "failed", "reason": str(e)})

    now = datetime.datetime.utcnow()
    expires = now + datetime.timedelta(minutes=body.ttl_minutes)

    with _quarantine_lock:
        _quarantine[ip] = {
            "reason": body.reason,
            "quarantined_at": now.isoformat(),
            "quarantined_by": user.email,
            "ttl_minutes": body.ttl_minutes,
            "expires_at": expires.isoformat(),
            "bidirectional": body.bidirectional,
            "rules_applied": rules_applied,
        }

    _log_audit(db, user, "QUARANTINE_ADD", f"ip={ip} reason={body.reason} ttl={body.ttl_minutes}m")

    return {
        "status": "quarantined",
        "ip": ip,
        "reason": body.reason,
        "expires_at": expires.isoformat(),
        "rules_applied": rules_applied,
    }


@router.delete("/quarantine/{ip}")
def unquarantine_ip(
    ip: str,
    user=Depends(require_role("admin")),
    db: Session = Depends(get_db),
):
    """Remove quarantine from an IP address (admin only)."""
    if not _validate_ip(ip):
        raise HTTPException(400, f"Invalid IP address: {ip}")

    with _quarantine_lock:
        if ip not in _quarantine:
            raise HTTPException(404, f"IP {ip} is not quarantined")
        entry = _quarantine.pop(ip)

    # Remove rules
    results = []
    del_in = f'iptables -D INPUT -s {ip} -j DROP -m comment --comment "RobustIDPS-Quarantine"'
    try:
        r = subprocess.run(del_in.split(), capture_output=True, text=True, timeout=10)
        results.append({"direction": "inbound", "status": "removed" if r.returncode == 0 else "failed"})
    except Exception as e:
        results.append({"direction": "inbound", "status": "failed", "detail": str(e)})

    if entry.get("bidirectional"):
        del_out = f'iptables -D OUTPUT -d {ip} -j DROP -m comment --comment "RobustIDPS-Quarantine"'
        try:
            r = subprocess.run(del_out.split(), capture_output=True, text=True, timeout=10)
            results.append({"direction": "outbound", "status": "removed" if r.returncode == 0 else "failed"})
        except Exception as e:
            results.append({"direction": "outbound", "status": "failed", "detail": str(e)})

    _log_audit(db, user, "QUARANTINE_REMOVE", f"ip={ip}")

    return {"status": "unquarantined", "ip": ip, "results": results}


